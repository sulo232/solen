// Top services horizontal bar chart
// ---------------------------------------------------------------------------

/**
 * @param {HTMLElement} container
 * @param {{ name: string, count: number }[]} topServices
 */
export function renderTopServicesChart(container, topServices) {
  if (!topServices.length) {
    container.innerHTML = '<p class="dash-chart-empty">Noch keine Daten</p>';
    return;
  }

  const max = Math.max(topServices[0].count || 1, 1);

  const html = `
    <div class="dash-chart">
      <h4 class="dash-chart-title">Top Services</h4>
      <div class="dash-chart-h-bars">
        ${topServices.map(({ name, count }) => {
          // name comes from DB — escape it
          const widthPct = ((Math.max(0, Number(count)) / max) * 100).toFixed(1);
          return `
            <div class="dash-chart-h-row">
              <div class="dash-chart-h-label">${esc(name)}</div>
              <div class="dash-chart-h-track">
                <div class="dash-chart-h-fill" style="width:${widthPct}%"></div>
              </div>
              <div class="dash-chart-h-value">${Number(count).toFixed(0)}\xd7</div>
            </div>`;
        }).join('')}
      </div>
    </div>`;

  container.innerHTML = html;
}

// ---------------------------------------------------------------------------
// Peak hours bar chart
// ---------------------------------------------------------------------------

/**
 * @param {HTMLElement} container
 * @param {number[]} hourCounts — 24-element array (index = hour of day)
 */
export function renderPeakHoursChart(container, hourCounts) {
  const businessHours = (hourCounts || []).slice(8, 21); // 08:00–20:00
  const max = Math.max(...businessHours, 1);

  const html = `
    <div class="dash-chart">
      <h4 class="dash-chart-title">Sto\xdfzeiten</h4>
      <div class="dash-chart-bars">
        ${businessHours.map((count, i) => {
          // i and count are JavaScript numbers — not user data
          const hour = i + 8;
          const heightPct = ((Math.max(0, Number(count)) / max) * 100).toFixed(1);
          const countSafe = Number(count).toFixed(0);
          return `
            <div class="dash-chart-bar-wrap" style="flex:1">
              <div class="dash-chart-bar" style="height:${heightPct}%" title="${hour}:00 \u2014 ${countSafe} Buchungen"></div>
              <div class="dash-chart-bar-label">${hour}</div>
            </div>`;
        }).join('')}
      </div>
    </div>`;

  container.innerHTML = html;
}

// ---------------------------------------------------------------------------
// Helpers (pure JS, no user data)
// ---------------------------------------------------------------------------

function buildDayBuckets(days) {
  const buckets = {};
  for (let i = days - 1; i >= 0; i--) {
    const d = new Date();
    d.setDate(d.getDate() - i);
    buckets[d.toISOString().slice(0, 10)] = 0;
  }
  return buckets;
}

function todayStr() {
  return new Date().toISOString().slice(0, 10);
}

function formatShortDate(dateStr) {
  const d = new Date(dateStr);
  return `${d.getDate()}.${d.getMonth() + 1}.`;
}
